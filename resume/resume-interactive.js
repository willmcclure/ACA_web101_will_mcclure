function fadeout(title) {
  if (title.style.opacity === "0") {
    title.style.opacity = "1";
  } else {
    title.style.opacity = "0";
  }
}

function disappear(img) {
  if (img.style.opacity === '0') {
    img.style.opacity = '1'
  } else {img.style.opacity = '0';
  }
};

function appear(phone) {
  if (phone.style.opacity === "0") {
    phone.style.opacity = "1";
  } else { phone.style.opacity = "0";
  }
 }